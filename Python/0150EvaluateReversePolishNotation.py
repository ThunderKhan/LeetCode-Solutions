def evalRPN(tokens : list[str]) -> int:
    stack = []

    for token in tokens:
        if token == '+':
            stack.append(stack.pop() + stack.pop())
        elif token == '*':
            stack.append(stack.pop() * stack.pop())
        elif token == '-':
            a, b = stack.pop(), stack.pop()
            stack.append(b - a)
        elif token == '/':
            a, b = stack.pop(), stack.pop()
            stack.append(int(b / a))
        else:
            stack.append(int(token))
    
    return stack[0]

tokens = ['2', '1', '+', '3', '*']
print(evalRPN(tokens))